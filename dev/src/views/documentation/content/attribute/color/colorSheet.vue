<script>
import hex from '@/lib/hex'
import copy from '@/lib/copy'

export default {
    name: "colorSheet",
    mounted(){
        let bars = document.getElementsByClassName('bar')
        for(let i=0;i<bars.length;i++){
            let bar = bars[i]
            let clr = hex(window.getComputedStyle(bar).backgroundColor)
            bar.getElementsByClassName('hex')[0].innerText = '#' + clr
        }
        let cpys = document.getElementsByClassName('cpy')
        for(let i=0;i<cpys.length;i++){
            cpys[i].onclick = () => {
                copy(cpys[i].innerText)
                this.$notify({
                    text: '”' + cpys[i].innerText + '“ 已复制到粘贴板',
                    color:'green',
                    close:false,
                })
            }
        }
    },
}
</script>

